package ru.netology.stats;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.Test;

public class StatsServiceTest {

    @Test
    public void testgeneralsum() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long atual = service.sum(sales);
        long expected = 8 + 15 + 13 + 15 + 17 + 20 + 19 + 20 + 7 + 14 + 14 + 18;

        Assertions.assertEquals(expected, atual);
    }

    @Test
    public void testmid() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long atual = service.mid(sales);
        long expected = (8 + 15 + 13 + 15 + 17 + 20 + 19 + 20 + 7 + 14 + 14 + 18) / 12;

        Assertions.assertEquals(expected, atual);
    }

    @Test
    public void testmonthmaxsum() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long atual = service.monthMaxsum(sales);
        long expected = 8;

        Assertions.assertEquals(expected, atual);
    }

    @Test
    public void testmonthminsum() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long atual = service.monthMinsum(sales);
        long expected = 9;

        Assertions.assertEquals(expected, atual);
    }

    @Test
    public void testmonthbelowaverage() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long atual = service.monthBelowAverage(sales);
        long expected = 5;

        Assertions.assertEquals(expected, atual);
    }

    @Test
    public void testmonthaboveaverage() {
        StatsService service = new StatsService();
        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};

        long atual = service.monthAboveAverage(sales);
        long expected = 5;

        Assertions.assertEquals(expected, atual);
    }
}
